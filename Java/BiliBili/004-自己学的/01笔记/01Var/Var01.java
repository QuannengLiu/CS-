public class Var01{
	public static void main(String[] args){
		int a = 1;
		System.out.println(a);
		a = 2;
		System.out.println(a);

	}
}
/*1.变量三要素：变量名、变量类型、变量值；
2.变量名命名：必须符合规则，最好符合规范；
3.变量名赋值：1.必须赋值2.值与变量类型必须相等3.值不能超过变量取值范围；
4.声明和赋值可以分开完成，也可以一起完成；
5.允许一次声明多个同类型的变量；
6.在同一个大括号当中不能同时声明多个同名的变量，但可以多次赋值；
7.变量必须先声明，然后才能使用，不能反过来；



*/